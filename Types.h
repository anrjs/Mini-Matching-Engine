#pragma once

#include <cstdint>

// ============================================================================
// 타입 별칭 (Type Aliases)
// ============================================================================

// 가격 타입: 소수점 오차 방지를 위해 정수형 사용
using Price = int32_t;

// 수량 타입: 수량은 음수가 될 수 없으므로 부호 없는 정수형 사용
using Quantity = uint32_t;

// 주문 ID 타입: 주문량이 많을 것을 대비하여 64비트 부호 없는 정수형 사용
using OrderId = uint64_t;

// ============================================================================
// 열거형 (Enumerations)
// ============================================================================

// 매수/매도 구분
enum class Side {
    BUY,   // 매수
    SELL   // 매도
};

// 주문 유형
enum class OrderType {
    LIMIT,   // 지정가 주문
    MARKET,  // 시장가 주문
    CANCEL   // 취소 주문
};
